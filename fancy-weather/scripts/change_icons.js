function icon(iconNum) {
  if (iconNum === '01d') { icons = './img/animated/day.svg'; }
  if (iconNum === '01n') { icons = './img/animated/night.svg'; }
  if (iconNum === '02d') { icons = './img/animated/cloudy-day-2.svg'; }
  if (iconNum === '02n') { icons = './img/animated/cloudy-night-2.svg'; }
  if (iconNum === '03d' || iconNum === '03n' || iconNum === '04d' || iconNum === '04n') { icons = './img/animated/cloudy.svg'; }
  if (iconNum === '09d' || iconNum === '09n') { icons = './img/animated/rainy-6.svg'; }
  if (iconNum === '10d') { icons = './img/animated/rainy-2.svg'; }
  if (iconNum === '10n') { icons = './img/animated/rainy-4.svg'; }
  if (iconNum === '11d' || iconNum === '11n') { icons = './img/animated/thunder.svg'; }
  if (iconNum === '11d' || iconNum === '11n') { icons = './img/animated/snowy-5.svg'; }
  if (iconNum === '50d' || iconNum === '50n') { icons = './img/animated/GPJ.svg'; }
}
